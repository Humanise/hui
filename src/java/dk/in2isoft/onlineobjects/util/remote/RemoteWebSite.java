package dk.in2isoft.onlineobjects.util.remote;

public class RemoteWebSite {
	
	private String title;
	
	private String url;
	
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTitle() {
		return title;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getUrl() {
		return url;
	}
}
