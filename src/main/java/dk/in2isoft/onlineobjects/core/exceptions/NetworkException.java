package dk.in2isoft.onlineobjects.core.exceptions;

public class NetworkException extends EndUserException {

	private static final long serialVersionUID = 1449397281498175390L;

	public NetworkException() {
		super();
	}

	public NetworkException(String arg0) {
		super(arg0);
	}

	public NetworkException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NetworkException(Throwable arg0) {
		super(arg0);
	}

}
