package dk.in2isoft.onlineobjects.core.exceptions;

public class ModelException extends EndUserException {

	private static final long serialVersionUID = 1449397281498175390L;

	public ModelException() {
		super();
	}

	public ModelException(String arg0) {
		super(arg0);
	}

	public ModelException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ModelException(Throwable arg0) {
		super(arg0);
	}

}
